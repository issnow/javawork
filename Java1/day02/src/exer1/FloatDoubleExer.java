package exer1;
/*
定义圆周率并赋值3.14,有3个圆的半径分别为1.2 2.5 6计算面积
 */
public class FloatDoubleExer {
  public static void main(String[] args) {
    double pi = 3.14;
    double r1 = 1.2;
    double r2 = 2.5;
    int r3 = 6;
    System.out.println(pi*r1*r1);
    System.out.println(pi*r2*r2);
    System.out.println(pi*r3*r3);
  }
}
